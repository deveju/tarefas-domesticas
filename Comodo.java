package app;

public class Comodo {
	protected String name;
	protected boolean temTarefas;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	public boolean isTemTarefas() {
		return temTarefas;
	}
	public void setTemTarefas(boolean temTarefas) {
		this.temTarefas = temTarefas;
	}
}
